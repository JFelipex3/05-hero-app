import { render, screen, waitFor } from "@testing-library/react";
import { beforeEach, describe, expect, it, vi } from "vitest";
import { SearchPage } from "./SearchPage";
import { QueryClient, QueryClientProvider } from "@tanstack/react-query";
import { MemoryRouter } from "react-router";
import { searchHeroesAction } from "@/heroes/actions/search-heroes.action";
import type { Hero } from "@/heroes/types/hero.interface";

vi.mock('@/heroes/actions/search-heroes.action');
const mockSearchHeroesAction = vi.mocked(searchHeroesAction);

vi.mock('@/components/custom/CustomJumbotron', () => ({
  CustomJumbotron: () => {
    <div data-testid="custom-jumbotron"></div>
  }
}));

vi.mock('@/heroes/components/HeroGrid', () => ({
  HeroGrid: ({heroes}: {heroes: Hero[]}) => (
    <div data-testid="hero-grid">
      {
        heroes.map(hero => (
          <div key={ hero.id }>{ hero.name }</div>
        ))
      }
    </div>
  )
}));

const queryClient = new QueryClient();

const renderSearchPage = (initialEntries: string[] = ['/']) => {

  return render(
    <MemoryRouter initialEntries={initialEntries}>
      <QueryClientProvider client={queryClient}>
        <SearchPage />
      </QueryClientProvider>
    </MemoryRouter>
  )
};

describe('SearchPage', () => {

  beforeEach(() => {
    vi.clearAllMocks();
  });

  it('should render SearchPage with default values', () => {
    const { container }  = renderSearchPage();

    expect(mockSearchHeroesAction).toHaveBeenCalledWith({ category: "", name: undefined, status: "", strength: "", team: "", universe: ""});
    expect(container).toMatchSnapshot();
  });

  it('should call search action with name parameter', () => {
    renderSearchPage(['/search?name=superman']);

    expect(mockSearchHeroesAction).toHaveBeenCalledWith({ category: "", name: "superman", status: "", strength: "", team: "", universe: ""});
  });

  it('should call search action with strength parameter', () => {
    renderSearchPage(['/search?strength=6']);

    expect(mockSearchHeroesAction).toHaveBeenCalledWith({ category: "", name: undefined, status: "", strength: "6", team: "", universe: ""});
  });

  it('should call search action with name and strength parameter', () => {
    renderSearchPage(['/search?name=batman&strength=8']);

    expect(mockSearchHeroesAction).toHaveBeenCalledWith({ category: "", name: "batman", status: "", strength: "8", team: "", universe: ""});
  });

  it('should render HeroGrid with search result', async () =>{
    const mockHeroes = [
      {id: 1, name: 'Clark Kent'} as unknown as Hero,
      {id: 2, name: 'Bruce Wayne'} as unknown as Hero
    ];

    mockSearchHeroesAction.mockResolvedValue(mockHeroes);

    renderSearchPage();

    await waitFor( () => {
      expect(screen.getByText('Clark Kent')).toBeDefined();
      expect(screen.getByText('Bruce Wayne')).toBeDefined();
    });
  });
});